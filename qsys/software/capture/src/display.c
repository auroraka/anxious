void drawChar(char c, int x, int y, unsigned color) {
	if ('a' <= c && c <= 'z') {
		drawLowercaseLetter(c, x, y, color);
	} else if ('A' <= c && c <= 'Z') {
		drawUppercaseLetter(c, x, y, color);
	} else if ('0' <= c && c <= '9') {
		drawDigit(c - '0', x, y, color);
	} else {
		int i;
		for (i = 0; i < 32; i++)
			if (symbols[i] == c) {
				drawSymbol(i, x, y, color);
				break;
			}
	}
}

void drawMsg(unsigned port, const char *str, int x, int y, unsigned color) {
	int i, px = 0, py = 0, len = strlen(str);
	display_overlay_port = port;
	for (i = 0; i < len; i++) {
		if (str[i] == '\n') ++py, px = 0;
		else {
			drawChar(str[i], y + py * 20, x + px * 9, color);
			++px;
		}
	}
}

void drawCursor(unsigned port, int x, int y) {
	int i, j;
	display_overlay_port = port;
	for (i = 0; i < CURSOR_H; i++) {
		for (j = 0; j < CURSOR_W; j++) {
			if (CURSOR[i][j] == '#') {
				OVERLAY_W(y + j, x + i, 0x323232);
			} else if (CURSOR[i][j] == '$') {
				OVERLAY_W(y + j, x + i, 0x505050);
			} else if (CURSOR[i][j] == '^') {
				OVERLAY_W(y + j, x + i, 0x7D7D7D);
			} else if (CURSOR[i][j] == '*') {
				OVERLAY_W(y + j, x + i, 0xFFFFFF);
			}
		}
	}
}

